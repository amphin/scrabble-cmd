class Tile(str, Enum):
    START = '*'
    EMPTY = ' '
    DOUBLE_LETTER = '2'
    TRIPLE_LETTER = '3'
    DOUBLE_WORD = '@'
    TRIPLE_WORD = 'ÂŁ'

    def __str__(self) -> str:
        return self.value

    def __repr__(self) -> str:
        return self.value

    def is_points_tile(tile: "Tile | str") -> bool:
        return tile in {
            Tile.DOUBLE_LETTER,
            Tile.TRIPLE_LETTER,
            Tile.DOUBLE_WORD,
            Tile.TRIPLE_WORD
        }


